fruit = ["banana", "apple", "mango"]
         
print("banana" in fruit)  # True, because "banana" is in the list
print("grape" in fruit)   # False, because "grape" is not in the list

print("grape" not in fruit)  # True, because "grape" is not in the list
print("banana" not in fruit)   # False, because "banana" is in the list   

sentence = "the quick brown fox jumps over the lazy dog"
print("fox" in sentence)  # True, because "fox" is in the sentence

print("cat" in sentence)  # False, because "cat" is not in the sentence